const hexaRegex = '#([A-Fa-f0-9]{8}|[A-Fa-f0-9]{6}|[A-Fa-f0-9]{4}|[A-Fa-f0-9]{3})'
const rgbaRegex = /rgba?\((\d{1,3}), ?(\d{1,3}), ?(\d{1,3})(, ?(1{1}|0?(\.\d{1,2})?))?\)/
const hslaRegex = /hsla?\((\d{1,3}), ?(\d{1,3})%, ?(\d{1,3})%(, ?(1{1}|0?(\.\d{1,2})?))?\)/

/**
 * @param {String} hexa Hexa css : #ffffffff
 * @returns {Array} Rgba values : [255, 255, 255, 1]
 */
function _hexaCssToRgba (hexa) {

  let r, g, b

  if (hexa.length >= 7) {
    r = '0x' + hexa[1] + hexa[2]
    g = '0x' + hexa[3] + hexa[4]
    b = '0x' + hexa[5] + hexa[6]
  } else {
    r = '0x' + hexa[1] + hexa[1]
    g = '0x' + hexa[2] + hexa[2]
    b = '0x' + hexa[3] + hexa[3]
  }

  let a

  if (hexa.length === 9) {
    a = '0x' + hexa[7] + hexa[8]
  } else if (hexa.length === 5) {
    a = '0x' + hexa[4] + hexa[4]
  } else {
    a = 1
  }

  if (a !== 1)
    a = +(a / 255).toFixed(2)

  return  [parseInt(r), parseInt(g), parseInt(b), parseFloat(a)]
}

/**
 * @param {String} hexa Hexa css : #ffffffff
 * @returns {Array} Rgba values : [255, 255, 255, 1]
 */
function _hexaCssToHsla (hexa) {

  const [r, g, b, a] = _hexaCssToRgba(hexa)

  const [h, s, l] = _rgbToHsl(r, g, b)

  return [h, s, l, a]
}

/**
 * @param {Integer} r Red value : 255
 * @param {Integer} g Green value : 255
 * @param {Integer} b Blue value : 255
 * @returns {Array} Hex values : ['ff', 'ff', 'ff']
 */
function _rgbToHex (r, g, b) {

  r = (+r).toString(16)
  g = (+g).toString(16)
  b = (+b).toString(16)

  if (r.length === 1)
    r = '0' + r
  if (g.length === 1)
    g = '0' + g
  if (b.length === 1)
    b = '0' + b

  return [r, g, b]
}

/**
 * @param {Integer} r Red value : 255
 * @param {Integer} g Green value : 255
 * @param {Integer} b Blue value : 255
 * @returns {Array} Hsl values : [0, 0, 100]
 */
function _rgbToHsl (r, g, b) {

  r /= 255
  g /= 255
  b /= 255

  let cmin = Math.min(r,g,b),
      cmax = Math.max(r,g,b),
      delta = cmax - cmin,
      h = 0,
      s = 0,
      l = 0

  if (delta == 0)
    h = 0
  else if (cmax === r)
    h = ((g - b) / delta) % 6
  else if (cmax === g)
    h = (b - r) / delta + 2
  else
    h = (r - g) / delta + 4

  h = Math.round(h * 60)

  if (h < 0)
      h += 360

  l = (cmax + cmin) / 2

  s = delta === 0 ? 0 : delta / (1 - Math.abs(2 * l - 1))

  s = +(s * 100).toFixed(1)
  l = +(l * 100).toFixed(1)

  return [parseInt(h), parseInt(s), parseInt(l)]
}

/**
 * @param {Integer} h Hue value : 0
 * @param {Integer} s Saturation value : 0
 * @param {Integer} l Lightness value : 100
 * @returns {Array} Hex values : ['ff', 'ff', 'ff']
 */
function _hslToHex (h, s, l) {

  s /= 100
  l /= 100

  let c = (1 - Math.abs(2 * l - 1)) * s,
      x = c * (1 - Math.abs((h / 60) % 2 - 1)),
      m = l - c/2

  let r, g, b

  if (0 <= h && h < 60) {
    r = c; g = x; b = 0;
  } else if (60 <= h && h < 120) {
    r = x; g = c; b = 0;
  } else if (120 <= h && h < 180) {
    r = 0; g = c; b = x;
  } else if (180 <= h && h < 240) {
    r = 0; g = x; b = c;
  } else if (240 <= h && h < 300) {
    r = x; g = 0; b = c;
  } else if (300 <= h && h < 360) {
    r = c; g = 0; b = x;
  }

  r = Math.round((r + m) * 255).toString(16)
  g = Math.round((g + m) * 255).toString(16)
  b = Math.round((b + m) * 255).toString(16)

  if (r.length === 1)
    r = '0' + r
  if (g.length === 1)
    g = '0' + g
  if (b.length === 1)
    b = '0' + b

  return [r, g, b]
}

/**
 * @param {Integer} h Hue value : 0
 * @param {Integer} s Saturation value : 0
 * @param {Integer} l Lightness value : 100
 * @returns {Array} Rgb values : [255, 255, 255]
 */
function _hslToRgb (h, s, l) {

  s /= 100;
  l /= 100;

  let c = (1 - Math.abs(2 * l - 1)) * s,
      x = c * (1 - Math.abs((h / 60) % 2 - 1)),
      m = l - c/2

  let r, g, b

  if (0 <= h && h < 60) {
    r = c; g = x; b = 0;
  } else if (60 <= h && h < 120) {
    r = x; g = c; b = 0;
  } else if (120 <= h && h < 180) {
    r = 0; g = c; b = x;
  } else if (180 <= h && h < 240) {
    r = 0; g = x; b = c;
  } else if (240 <= h && h < 300) {
    r = x; g = 0; b = c;
  } else if (300 <= h && h < 360) {
    r = c; g = 0; b = x;
  }

  r = Math.round((r + m) * 255)
  g = Math.round((g + m) * 255)
  b = Math.round((b + m) * 255)

  return [parseInt(r), parseInt(g), parseInt(b)]
}

/**
 * @param {String} hsla Hsla css : hsla(0, 0%, 100%, 1)
 * @returns {Array} Rgba values : [255, 255, 255, 1]
 */
function _hslaCssToRgba (hsla) {

  const [h, s, l, a] = _hslaCssToArray(hsla)

  const [r, g, b] = _hslToRgb(h, s, l)

  return [r, g, b, a]
}

/**
 * @param {String} rgba Rgba css : rgba(255, 255, 255, 1)
 * @returns {Array} Hsla values : [0, 0, 100, 1]
 */
function _rgbaCssToHsla (rgba) {

  const [r, g, b, a] = _rgbaCssToArray(rgba)

  const [h, s, l] = _rgbToHsl(r, g, b)

  return [h, s, l, a]
}

/**
 * @param {String} rgba Rgba css : rgba(255, 255, 255, 1)
 * @returns {Array} [255, 255, 255, 1]
 */
function _rgbaCssToArray (rgba) {

  const [, r, g, b, , a] = rgba.match(rgbaRegex)

  return [parseInt(r), parseInt(g), parseInt(b), typeof a !== 'undefined' ? parseFloat(a) : 1]
}

/**
 * @param {String} hsla Hsla css : hsla(0, 0%, 100%, 1)
 * @returns {Array} [0, 0, 100, 1]
 */
function _hslaCssToArray (hsla) {

  let [, h, s, l, , a] = hsla.match(hslaRegex)

  return [parseInt(h), parseInt(s), parseInt(l), typeof a !== 'undefined' ? parseFloat(a) : 1]
}

/**
 * @param {Integer} r Red value : 255
 * @param {Integer} g Green value : 255
 * @param {Integer} b Blue value : 255
 * @param {Float} a Alpha value : 1
 * @returns {(Array|null)} Rgba css : rgba(255, 255, 255, 1)
 */
export function matchColor (color) {

  let matches = color.match(new RegExp(hexaRegex))

  if (!matches)
    matches = color.match(rgbaRegex)

  if (!matches)
    matches = color.match(hslaRegex)

  return matches
}

/**
 * @param {Integer} r Red value : 255
 * @param {Integer} g Green value : 255
 * @param {Integer} b Blue value : 255
 * @param {Float} a Alpha value : 1
 * @returns {String} Rgba css : rgba(255, 255, 255, 1)
 */
export function rgbaToCss (r, g, b, a) {

  return `rgba(${r}, ${g}, ${b}, ${a})`
}

/**
 * @param {Integer} h Hue value : 0
 * @param {Integer} s Saturation value : 0
 * @param {Integer} l Lightness value : 100
 * @param {Float} a Alpha value : 1
 * @returns {String} Hsla css : hsla(0, 0%, 100%, 1)
 */
export function hslaToCss (h, s, l, a) {

  return `hsla(${h}, ${s}%, ${l}%, ${a})`
}

/**
 * @param {String} hexa Hex or hexa css
 * @returns {Boolean}
 */
export function hexaIsValid (hexa) {

  return new RegExp(`^${hexaRegex}$`).test(hexa)
}

/**
 * @param {String} rgba Rgba or rgb css
 * @returns {Boolean}
 */
export function rgbaIsValid (rgba) {

  const matches = rgba.match(rgbaRegex)

  if (!matches)
    return false

  const [, r, g, b, , a] = matches

  return r >= 0 && 255 >= r &&
         g >= 0 && 255 >= g &&
         b >= 0 && 255 >= b &&
         (typeof a === 'undefined' || (a >= 0 && 1 >= a))
}

/**
 * @param {String} hsla Hsla or hsl css
 * @returns {Boolean}
 */
export function hslaIsValid (hsla) {

  const matches = hsla.match(hslaRegex)

  if (!matches)
    return false

  const [, h, s, l, , a] = matches

  return h >= 0 && 360 >= h &&
         s >= 0 && 100 >= s &&
         l >= 0 && 100 >= l &&
         (typeof a === 'undefined' || (a >= 0 && 1 >= a))
  }

/**
 * Compare 2 css colors
 *
 * @param {String} color1 Hex, rgb(a) or hsl(a)
 * @param {String} color2 Hex, rgb(a) or hsl(a)
 * @returns {Boolean}
 */
export function colorIsEgual (color1, color2) {

  const [r1, g1, b1, a1] = colorToRgba(color1)
  const [r2, g2, b2, a2] = colorToRgba(color2)

  return r1 === r2 && g1 === g2 && b1 === b2 && a1 === a2
}

/**
 * @param {String} color Color css hex(a), rgb(a) or hsl(a)
 * @returns {String} Hexa css
 */
export function colorToHexa (color) {

  if (color.startsWith('rgb'))
    color = rgbaToHexa(color)
  else if (color.startsWith('hsl'))
    color = hslaToHexa(color)

  return color
}

/**
 * @param {String} color Color css hex(a), rgb(a) or hsl(a)
 * @returns {String} Rgba css
 */
export function colorToRgba (color) {

  if (color.startsWith('#'))
    return _hexaCssToRgba(color)
  else if (color.startsWith('hsl'))
    return _hslaCssToRgba(color)

  return _rgbaCssToArray(color)
}

/**
 * @param {String} color Color css hex(a), rgb(a) or hsl(a)
 * @returns {String} Hsla css
 */
export function colorToHsla (color) {

  if (color.startsWith('#'))
    return _hexaCssToHsla(color)
  else if (color.startsWith('rgb'))
    return _rgbaCssToHsla(color)

  return _hslaCssToArray(color)
}

/**
 * @param {String} hexa Hexa css : #ffffffff
 * @returns {String} Rgba css : rbga(255, 255, 255, 1)
 */
export function hexaToRgba (hexa) {

  const [r, g, b, a] = _hexaCssToRgba(hexa)

  return rgbaToCss(r, g, b, a)
}

/**
 * @param {String} hexa Hexa css : #ffffffff
 * @returns {String} Hsla css : hsla(0, 0%, 100%, 1)
 */
export function hexaToHsla (hexa) {

  const [h, s, l, a] = _hexaCssToHsla(hexa)

  return hslaToCss(h, s, l, a)
}

/**
 * @param {String} rgba Rgba css : rgba(255, 255, 255, 1)
 * @returns {String} Hexa css : #ffffffff
 */
export function rgbaToHexa(rgba) {

  const [r, g, b, a] = _rgbaCssToArray(rgba)

  const [hr, hg, hb] = _rgbToHex(r, g, b)

  let ha = Math.round(a * 255).toString(16)

  if (ha.length === 1)
    ha = '0' + a

  return `#${hr + hg + hb + ha}`
}

/**
 * @param {String} rgba Rgba css : rgba(255, 255, 255, 1)
 * @returns {String} Hsla css : hsla(0, 0%, 100%, 1)
 */
export function rgbaToHsla (rgba) {

  const [h ,s, l, a] = _rgbaCssToHsla(rgba)

  return hslaToCss(h, s, l, a)
}

/**
 * @param {String} hsla Hsla css : hsla(0, 0%, 100%, 1)
 * @returns {String} Hexa css : #ffffffff
 */
export function hslaToHexa (hsla) {

  let [h, s, l, a] = _hslaCssToArray(hsla)

  let [r, g, b] = _hslToHex(h, s, l)

  a = Math.round(a * 255).toString(16)

  if (r.length === 1)
    r = '0' + r
  if (g.length === 1)
    g = '0' + g
  if (b.length === 1)
    b = '0' + b
  if (a.length === 1)
    a = '0' + a

  return `#${r + g + b + a}`
}

/**
 * @param {String} hsla Hsla css : hsla(0, 0%, 100%, 1)
 * @returns {String} Rgba css : rgb(255, 255, 255, 1)
 */
export function hslaToRgba (hsla) {

  const [r, g, b, a] = _hslaCssToRgba(hsla)

  return rgbaToCss(r, g, b, a)
}
